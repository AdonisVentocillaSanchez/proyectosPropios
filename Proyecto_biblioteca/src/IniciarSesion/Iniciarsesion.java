    public boolean autenticarUsuario(String usuario, String contra){
        try {
            f = new File("usuarios.txt");
            fr = new FileReader(f);
            br = new BufferedReader(fr);
            String linea;
            while((linea = br.readLine()) != null){
                String datos[] = linea.split(",");
                user = datos[0];  
                pass = datos[1];   
                if(user.equals(usuario) && pass.equals(contra))
                    return true;
            }
            fr.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error = " + e.toString());
        }
        return false;
    }
